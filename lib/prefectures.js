export function getPrefectureCode(areaName) {
  for (const [pref, code] of Object.entries(PREFECTURE_CODES)) {
    if (areaName.includes(pref)) return code;
  }
  return null;
}

export function getCityCode(areaName) {
  // 長い文字列から優先的にマッチ（「福岡市中央区」が「中央区」より先にマッチするように）
  const sorted = Object.entries(CITY_CODES).sort((a, b) => b[0].length - a[0].length);
  for (const [city, code] of sorted) {
    if (areaName.includes(city)) return code;
  }
  return null;
}